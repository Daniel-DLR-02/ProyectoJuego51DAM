package Model;

public class Boton {

	int posy,posx;
	
	public Boton(int posy,int posx) {
		
		this.posx=posx;
		this.posy=posy;
	}

	public int getPosy() {
		return posy;
	}

	public void setPosy(int posy) {
		this.posy = posy;
	}

	public int getPosx() {
		return posx;
	}

	public void setPosx(int posx) {
		this.posx = posx;
	}
	
}
